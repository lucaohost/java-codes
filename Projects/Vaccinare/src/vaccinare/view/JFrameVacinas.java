    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        try {
            ArrayList<Vacina> vacinas = null;

            DefaultTableModel tableModel = (DefaultTableModel) jTableVacinas.getModel();
            while (tableModel.getRowCount() > 0) {
                tableModel.removeRow(0);
            }
            //Crio o Model de Vacinas
            Vacina vacina = new Vacina();

            //Realizo a pesquisa
            String nome = jTextFieldNomeVacina.getText();
            //E salvo em um array
            vacinas = vacina.select(nome);

            if (!vacinas.isEmpty()) {
                //percorre o array e adiciono ao DefaultTableModel
                for (Vacina v : vacinas) {
                    tableModel.addRow(new Object[]{v.getId(), v.getNomeVacina(), v.getNumeroLote()});
                }
                //Associo a o tableModel ao componente na tela
                jTableVacinas.setModel(tableModel);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Lamento, mas esta pesquisa nÃ£o retornou registros.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lamento, mas tivemos uma ocorrÃªncia inesperada no sistema.\n Erro:" + ex.toString(), "OcorrÃªncia Inesperada", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

    public boolean limparCampos() {

        Component componentes[] = jPanel1.getComponents();
        for (Component c : componentes) {
            if (c instanceof JTextField) {
                ((JTextField) c).setText("");
            } else if (c instanceof JComboBox) {
                ((JComboBox) c).setSelectedIndex(0);
            } else if (c instanceof JRadioButton) {
                ((JRadioButton) c).setSelected(true);

            }
        }
        return true;

    }
