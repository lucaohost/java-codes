    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        try {

            // Passa os dados da view para a Model
            Vacinacao vacinacao = new Vacinacao();
            Vacina vacina = new Vacina();
            vacina.setId(Integer.parseInt(jTextFieldIDVacina.getText()));
            Crianca cria = new Crianca();
            cria.setId(Integer.parseInt(jTextFieldIDCria.getText()));
            Usuario user = new Usuario();
            user.setId(Usuario.authId);

            vacinacao.setAplicador(user);
            vacinacao.setCrianca(cria);
            vacinacao.setVacina(vacina);

            String date = jTextFieldData.getText();
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            java.sql.Date data = new java.sql.Date(format.parse(date).getTime());

            vacinacao.setDataAplicacao(data);

            vacinacao.insert();

            JOptionPane.showMessageDialog(rootPane, "Vacinacão cadastrada com sucesso.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lamento, mas tivemos uma ocorrência inesperada no sistema.\n Erro:" + ex.toString(), "Ocorrência Inesperada", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed

        try {

            //Crio o array vazio de Criancas
            ArrayList<Crianca> criancas = null;

            //Pego o DefaultTableModel do componente de JTable do Java
            DefaultTableModel tableModel = (DefaultTableModel) jTableCriancas.getModel();
            while (tableModel.getRowCount() > 0) {
                tableModel.removeRow(0);
            }
            //Crio o Model de Crianca
            Crianca crianca = new Crianca();

            //Realizo a pesquisa
            String pesquisa = "";
            //E salvo em um array
            criancas = crianca.select(pesquisa);

            if (!criancas.isEmpty()) {
                //percorre o array e adiciono ao DefaultTableModel
                for (Crianca cr : criancas) {
                    tableModel.addRow(new Object[]{cr.getId(), cr.getNome(), cr.getIdade()});
                }
                //Associo a o tableModel ao componente na tela
                jTableCriancas.setModel(tableModel);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Lamento, mas esta pesquisa não retornou registros.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lamento, mas tivemos uma ocorrência inesperada no sistema.\n Erro:" + ex.toString(), "Ocorrência Inesperada", JOptionPane.ERROR_MESSAGE);
        }

        try {
            ArrayList<Vacina> vacinas = null;

            DefaultTableModel tableModel = (DefaultTableModel) jTableVacinas.getModel();
            while (tableModel.getRowCount() > 0) {
                tableModel.removeRow(0);
            }
            //Crio o Model de Vacinas
            Vacina vacina = new Vacina();

            //Realizo a pesquisa
            String pesquisa = "";
            //E salvo em um array
            vacinas = vacina.select(pesquisa);

            if (!vacinas.isEmpty()) {
                //percorre o array e adiciono ao DefaultTableModel
                for (Vacina v : vacinas) {
                    tableModel.addRow(new Object[]{v.getId(), v.getNomeVacina(), v.getNumeroLote()});
                }
                //Associo a o tableModel ao componente na tela
                jTableVacinas.setModel(tableModel);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Lamento, mas esta pesquisa não retornou registros.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lamento, mas tivemos uma ocorrência inesperada no sistema.\n Erro:" + ex.toString(), "Ocorrência Inesperada", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

