    private void jButtonPesquisar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisar1ActionPerformed
        try {
            ArrayList<Vacinacao> vacinacoes = null;

            DefaultTableModel tableModel = (DefaultTableModel) jTableVacinacoes.getModel();
            while (tableModel.getRowCount() > 0) {
                tableModel.removeRow(0);
            }
            //Crio o Model de Vacinas
            Vacinacao vacinacao = new Vacinacao();

            //Realizo a pesquisa
            String nome = jTextFieldNomeCria.getText();
            //E salvo em um array
            vacinacoes = vacinacao.select(nome);

            if (!vacinacoes.isEmpty()) {
                //percorre o array e adiciono ao DefaultTableModel
                for (Vacinacao vc : vacinacoes) {
                    tableModel.addRow(new Object[]{vc.getId(), vc.getCrianca().getNome(), vc.getAplicador().getLogin()});
                }
                //Associo a o tableModel ao componente na tela
                jTableVacinacoes.setModel(tableModel);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Lamento, mas esta pesquisa não retornou registros.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lamento, mas tivemos uma ocorrência inesperada no sistema.\n Erro:" + ex.toString(), "Ocorrência Inesperada", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPesquisar1ActionPerformed

