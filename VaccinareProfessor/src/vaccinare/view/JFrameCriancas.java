    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        try {
            
            
            // Passa os dados da view para a Model
            Crianca crianca = new Crianca();
            
            crianca.setNome(jTextFieldNome.getText());
            crianca.setIdade(Integer.parseInt(jTextFieldIdade.getText()));
            crianca.setSexo(jComboBoxSexo.getSelectedItem().toString());
            crianca.setPartoNatural(jCheckBoxParto.isSelected());
            crianca.setMae(jTextFieldMae.getText());
            if(jRadioButtonBranca.isSelected()) crianca.setEtnia("branca");
            if(jRadioButtonAmarela.isSelected()) crianca.setEtnia("amarela");
            if(jRadioButtonIndigena.isSelected()) crianca.setEtnia("indigena");
            if(jRadioButtonNegra.isSelected()) crianca.setEtnia("negra");
            if(jRadioButtonParda.isSelected()) crianca.setEtnia("parda");
                
            // Chama o método inserir
            crianca.inserir();
            
            JOptionPane.showMessageDialog(rootPane, "Criança cadastrada com sucesso.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lamento, mas tivemos uma ocorrência inesperada no sistema.\n Erro:"+ex.toString(), "Ocorrência Inesperada", JOptionPane.ERROR_MESSAGE);            
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

