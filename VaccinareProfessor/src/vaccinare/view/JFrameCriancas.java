    private void jTableCriancasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCriancasMouseClicked
         try {
            //Pego a linha selecionada na tela
            int row = jTableCriancas.getSelectedRow();
            
            //Pego o DefaultTableModel do componente de JTable do Java
            DefaultTableModel  tableModel = (DefaultTableModel) jTableCriancas.getModel();
            //pego o ID da Crianca pela tela
            int id = (int) tableModel.getValueAt(row, 0);
            //Crio a viagem vazia
            Crianca crianca = new Crianca();
            //Pesquiso e retorno um chamado com os dados do banco
            crianca.buscar(id);
            
            //Coloco os dados que vieram no banco no formulário da tela
            jTextFieldID.setText(String.valueOf(crianca.getId()));
            jTextFieldNome.setText(crianca.getNome());
            jTextFieldIdade.setText(String.valueOf(crianca.getIdade()));
            jComboBoxSexo.setSelectedItem(crianca.getSexo());
            jCheckBoxParto.setSelected(crianca.isPartoNatural());
            jTextFieldMae.setText(crianca.getMae());
            switch (crianca.getEtnia()){
                case "amarela":
                    jRadioButtonAmarela.setSelected(true);
                    break;
                case "branca":
                    jRadioButtonBranca.setSelected(true);
                    break;
                case "indigena":
                    jRadioButtonIndigena.setSelected(true);
                    break;
                case "negra":
                    jRadioButtonNegra.setSelected(true);
                    break;
                case "parda":
                    jRadioButtonParda.setSelected(true);
                    break;
            }
                      
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Lamento, mas tivemos uma ocorrência inesperada no sistema.\n Erro:"+ex.toString(), "Ocorrência Inesperada", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTableCriancasMouseClicked

